    fun escape(escapeChar: Byte): Byte {
        if (escapeChar < 0) {
            throw JsonParsingException("Escaped unexpected character: ${escapeChar.toInt().toChar()}")
        }
        val escapeResult = ESCAPE_MAP[escapeChar.toInt()]
        if (escapeResult == 0.toByte()) {
            throw JsonParsingException("Escaped unexpected character: ${escapeChar.toInt().toChar()}")
        }
        return escapeResult
    }

