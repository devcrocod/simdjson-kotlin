    // Efficient approach for parsing 4-byte hex strings as described at:
    // https://lemire.me/blog/2019/04/17/parsing-short-hexadecimal-strings-efficiently/
    fun hexToInt(buff: ByteArray, offset: Int): Int {
        val v1 = HEX_DIGIT_TO_INT[630 + getUnsignedByte(buff, offset)]
        val v2 = HEX_DIGIT_TO_INT[420 + getUnsignedByte(buff, offset + 1)]
        val v3 = HEX_DIGIT_TO_INT[210 + getUnsignedByte(buff, offset + 2)]
        val v4 = HEX_DIGIT_TO_INT[getUnsignedByte(buff, offset + 3)]
        return v1 or v2 or v3 or v4
    }

    private fun getUnsignedByte(buff: ByteArray, idx: Int): Int {
        return buff[idx].toInt() and 0xFF
    }
